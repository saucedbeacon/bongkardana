package androidx.browser.customtabs;

import android.content.Intent;
import android.os.Bundle;
import android.util.SparseArray;
import androidx.annotation.NonNull;
import androidx.annotation.Nullable;
import androidx.annotation.RestrictTo;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.util.ArrayList;
import o.rate;

public final class CustomTabsIntent {
    @NonNull
    public final Intent length;
    @Nullable
    public final Bundle setMin;

    @RestrictTo({RestrictTo.Scope.LIBRARY})
    @Retention(RetentionPolicy.SOURCE)
    public @interface ColorScheme {
    }

    @RestrictTo({RestrictTo.Scope.LIBRARY})
    @Retention(RetentionPolicy.SOURCE)
    public @interface ShareState {
    }

    public static final class getMin {
        @Nullable
        public SparseArray<Bundle> IsOverlapping;
        @Nullable
        public Bundle equals;
        @Nullable
        public Bundle getMax;
        @Nullable
        public ArrayList<Bundle> getMin;
        public final rate.length length = new rate.length();
        public final Intent setMax = new Intent("android.intent.action.VIEW");
        @Nullable
        public ArrayList<Bundle> setMin;
        public int toFloatRange = 0;
        public boolean toIntRange = true;
    }

    public CustomTabsIntent(@NonNull Intent intent, @Nullable Bundle bundle) {
        this.length = intent;
        this.setMin = bundle;
    }
}
