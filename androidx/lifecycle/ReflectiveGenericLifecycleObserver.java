package androidx.lifecycle;

import androidx.annotation.NonNull;
import androidx.lifecycle.Lifecycle;
import o.initialize;
import o.setChecked;
import o.setPopupCallback;

public class ReflectiveGenericLifecycleObserver implements setPopupCallback {
    private final initialize.setMin getMax = initialize.getMax.getMin(this.getMin.getClass());
    private final Object getMin;

    public ReflectiveGenericLifecycleObserver(Object obj) {
        this.getMin = obj;
    }

    public void setMax(@NonNull setChecked setchecked, @NonNull Lifecycle.Event event) {
        initialize.setMin setmin = this.getMax;
        Object obj = this.getMin;
        initialize.setMin.length(setmin.length.get(event), setchecked, event, obj);
        initialize.setMin.length(setmin.length.get(Lifecycle.Event.ON_ANY), setchecked, event, obj);
    }
}
