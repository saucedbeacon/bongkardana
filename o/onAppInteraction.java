package o;

import o.addAnimatorUpdateListener;

final class onAppInteraction implements addAnimatorUpdateListener.getMax {
    private final AppLoadInterceptorPoint length;

    public onAppInteraction(AppLoadInterceptorPoint appLoadInterceptorPoint) {
        this.length = appLoadInterceptorPoint;
    }

    public final Object processFacade(Object obj) {
        return ((AppInteractionPoint) obj).fetchActivity(this.length);
    }
}
