package o;

import java.io.IOException;
import o.Framedata;
import o.increaseBuffer;

public final class initMessage implements increaseBuffer {
    public final generateKey setMax;

    public initMessage(generateKey generatekey) {
        this.setMax = generatekey;
    }

    public final InvalidHandshakeException getMax(increaseBuffer.setMax setmax) throws IOException {
        Framedata.Opcode opcode = (Framedata.Opcode) setmax;
        InvalidDataException invalidDataException = opcode.setMax;
        isFin isfin = opcode.getMax;
        return opcode.length(invalidDataException, isfin, isfin.getMin(this.setMax, setmax, !invalidDataException.getMin.equals("GET")), isfin.length());
    }
}
