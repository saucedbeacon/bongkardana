package o;

public final class postNotification implements pathIsInAppScope {
    private String getMax;
    private checkFilePath length;

    public postNotification(String str, checkFilePath checkfilepath) {
        this.getMax = str;
        this.length = checkfilepath;
    }

    public final void getMin() {
        checkFilePath checkfilepath = this.length;
        if (checkfilepath != null) {
            checkfilepath.getMin(this.getMax);
        }
    }
}
