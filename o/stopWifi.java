package o;

import id.dana.onboarding.splash.SplashActivity;

public final class stopWifi implements setHorizontalGravity {
    private final SplashActivity setMin;

    public stopWifi(SplashActivity splashActivity) {
        this.setMin = splashActivity;
    }

    public final void length(Object obj) {
        SplashActivity.length(this.setMin, (setVerticalGravity) obj);
    }
}
