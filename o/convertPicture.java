package o;

import id.dana.richview.category.CategoryView;
import o.Ignore;

public final class convertPicture implements Ignore.setMin {
    private final CategoryView getMin;

    public convertPicture(CategoryView categoryView) {
        this.getMin = categoryView;
    }

    public final void length(int i) {
        CategoryView.setMax(this.getMin, i);
    }
}
