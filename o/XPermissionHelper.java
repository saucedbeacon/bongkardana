package o;

import id.dana.sendmoney.contact.recent.RecentContactView;
import java.util.List;
import o.onLoadFailed;

public final class XPermissionHelper implements onLoadFailed.length {
    private final RecentContactView setMax;

    public XPermissionHelper(RecentContactView recentContactView) {
        this.setMax = recentContactView;
    }

    public final void length(List list) {
        this.setMax.recentContactAdapter.getMin(list);
    }
}
