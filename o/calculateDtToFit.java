package o;

import android.location.Location;
import androidx.annotation.NonNull;
import com.otaliastudios.cameraview.controls.Facing;
import com.otaliastudios.cameraview.controls.PictureFormat;

public class calculateDtToFit {
    private final PictureFormat IsOverlapping;
    private final byte[] equals;
    private final Location getMax;
    public final int getMin;
    private final boolean length;
    private final Facing setMax;
    private final shouldDeferAccessibilityEvent setMin;

    public static class length {
        public shouldDeferAccessibilityEvent getMax;
        public boolean getMin;
        public byte[] isInside;
        public int length;
        public Location setMax;
        public Facing setMin;
        public PictureFormat toIntRange;
    }

    public calculateDtToFit(@NonNull length length2) {
        this.length = length2.getMin;
        this.getMax = length2.setMax;
        this.getMin = length2.length;
        this.setMin = length2.getMax;
        this.setMax = length2.setMin;
        this.equals = length2.isInside;
        this.IsOverlapping = length2.toIntRange;
    }

    @NonNull
    public final byte[] length() {
        return this.equals;
    }
}
