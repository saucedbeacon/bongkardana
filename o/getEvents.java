package o;

import id.dana.myprofile.MyProfileFragment;
import o.Ignore;

public final class getEvents implements Ignore.setMin {
    private final MyProfileFragment setMax;

    public getEvents(MyProfileFragment myProfileFragment) {
        this.setMax = myProfileFragment;
    }

    public final void length(int i) {
        MyProfileFragment.getMin(this.setMax, i);
    }
}
