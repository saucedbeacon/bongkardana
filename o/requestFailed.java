package o;

import id.dana.nearbyme.adapter.MerchantListAdapter;

public final class requestFailed implements setIcon {
    private final MerchantListAdapter getMax;

    public requestFailed(MerchantListAdapter merchantListAdapter) {
        this.getMax = merchantListAdapter;
    }

    public final void getMax(Object obj) {
        this.getMax.getMin.setMin(((Integer) obj).intValue());
    }
}
