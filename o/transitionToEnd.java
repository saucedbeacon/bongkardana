package o;

import androidx.annotation.NonNull;
import java.io.File;
import o.getConstraintSetNames;

public final class transitionToEnd<DataType> implements getConstraintSetNames.setMax {
    private final DataType getMax;
    private final setPivotX<DataType> length;
    private final MotionHelper setMax;

    public transitionToEnd(setPivotX<DataType> setpivotx, DataType datatype, MotionHelper motionHelper) {
        this.length = setpivotx;
        this.getMax = datatype;
        this.setMax = motionHelper;
    }

    public final boolean getMin(@NonNull File file) {
        return this.length.setMax(this.getMax, file, this.setMax);
    }
}
