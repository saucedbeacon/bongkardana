package o;

public abstract class shouldOverrideKeyEvent extends parseFailed<onEmbedViewDestory, setMax> {
    public shouldOverrideKeyEvent(appxLoadFailed appxloadfailed, getScheme getscheme) {
        super(appxloadfailed, getscheme);
    }

    public static class setMax {
        String maxId;
        int pageSize;

        private setMax(int i, String str) {
            this.pageSize = i;
            this.maxId = str;
        }

        public static setMax forGetFeeds(int i, String str) {
            return new setMax(i, str);
        }
    }
}
