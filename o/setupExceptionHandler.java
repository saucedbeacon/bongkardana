package o;

final class setupExceptionHandler implements RedDotDrawable {
    private final setExtensionCreator getMax;

    public setupExceptionHandler(setExtensionCreator setextensioncreator) {
        this.getMax = setextensioncreator;
    }

    public final Object apply(Object obj) {
        return this.getMax.transform((toJSONString) obj);
    }
}
