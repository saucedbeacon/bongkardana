package o;

import androidx.annotation.NonNull;

public final class updateHierarchy extends setMinWidth<getViewWidget> {
    public updateHierarchy(getViewWidget getviewwidget) {
        super(getviewwidget);
    }

    @NonNull
    public final Class<getViewWidget> getMin() {
        return getViewWidget.class;
    }

    public final int setMax() {
        resolveSystem resolvesystem = ((getViewWidget) this.getMin).getMax.getMin;
        return resolvesystem.getMax.toFloatRange() + resolvesystem.toDoubleRange;
    }

    public final void length() {
        ((getViewWidget) this.getMin).stop();
        getViewWidget getviewwidget = (getViewWidget) this.getMin;
        getviewwidget.setMax = true;
        resolveSystem resolvesystem = getviewwidget.getMax.getMin;
        resolvesystem.setMin.clear();
        if (resolvesystem.IsOverlapping != null) {
            resolvesystem.setMax.setMin(resolvesystem.IsOverlapping);
            resolvesystem.IsOverlapping = null;
        }
        resolvesystem.getMin = false;
        if (resolvesystem.toFloatRange != null) {
            resolvesystem.length.getMax((layoutChildWithAnchor<?>) resolvesystem.toFloatRange);
            resolvesystem.toFloatRange = null;
        }
        if (resolvesystem.isInside != null) {
            resolvesystem.length.getMax((layoutChildWithAnchor<?>) resolvesystem.isInside);
            resolvesystem.isInside = null;
        }
        if (resolvesystem.toIntRange != null) {
            resolvesystem.length.getMax((layoutChildWithAnchor<?>) resolvesystem.toIntRange);
            resolvesystem.toIntRange = null;
        }
        resolvesystem.getMax.toIntRange();
        resolvesystem.equals = true;
    }

    public final void getMax() {
        ((getViewWidget) this.getMin).getMax.getMin.IsOverlapping.prepareToDraw();
    }
}
