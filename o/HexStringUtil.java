package o;

import java.util.List;

public class HexStringUtil extends initWithSecurityGuard {
    private boolean hasMore;
    private String maxId;
    private List<decodeHex> notifications;

    public boolean isHasMore() {
        return this.hasMore;
    }

    public void setHasMore(boolean z) {
        this.hasMore = z;
    }

    public String getMaxId() {
        return this.maxId;
    }

    public void setMaxId(String str) {
        this.maxId = str;
    }

    public List<decodeHex> getNotifications() {
        return this.notifications;
    }

    public void setNotifications(List<decodeHex> list) {
        this.notifications = list;
    }
}
