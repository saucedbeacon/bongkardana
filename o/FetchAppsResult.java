package o;

import id.dana.nearbyme.adapter.MerchantListAdapter;
import id.dana.nearbyme.model.ShopModel;

public final class FetchAppsResult implements setIcon {
    private final MerchantListAdapter length;

    public FetchAppsResult(MerchantListAdapter merchantListAdapter) {
        this.length = merchantListAdapter;
    }

    public final void getMax(Object obj) {
        this.length.getMax.getMin(((ShopModel) this.length.setMin(((Integer) obj).intValue())).toDoubleRange);
    }
}
