package o;

import androidx.annotation.Nullable;

public class setTitleMarginBottom<T> {
    @Nullable
    protected T length = null;
    public final onRtlPropertiesChanged<T> setMax = new onRtlPropertiesChanged<>();
    @Nullable
    public setInputType<?, ?> setMin;

    public setTitleMarginBottom() {
    }

    public setTitleMarginBottom(@Nullable T t) {
        this.length = t;
    }

    @Nullable
    public T getMax() {
        return this.length;
    }
}
