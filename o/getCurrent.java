package o;

import android.view.Menu;
import android.view.MenuInflater;
import android.view.MenuItem;
import androidx.annotation.NonNull;

public class getCurrent {
    public final getOpacity<?> getMin;

    @NonNull
    public static getCurrent getMax(@NonNull getOpacity<?> getopacity) {
        return new getCurrent(getopacity);
    }

    private getCurrent(getOpacity<?> getopacity) {
        this.getMin = getopacity;
    }

    public final boolean length(@NonNull Menu menu, @NonNull MenuInflater menuInflater) {
        return this.getMin.getMax.getMin(menu, menuInflater);
    }

    public final boolean setMin(@NonNull Menu menu) {
        return this.getMin.getMax.getMin(menu);
    }

    public final boolean getMax(@NonNull MenuItem menuItem) {
        return this.getMin.getMax.getMax(menuItem);
    }

    public final boolean getMin(@NonNull MenuItem menuItem) {
        return this.getMin.getMax.length(menuItem);
    }

    public final boolean setMax() {
        return this.getMin.getMax.setMax(true);
    }
}
