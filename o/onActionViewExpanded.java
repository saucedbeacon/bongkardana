package o;

import android.graphics.PointF;
import java.util.List;
import o.SearchView;

public final class onActionViewExpanded implements SearchView.SavedState<PointF, PointF> {
    private final onTextFocusChanged setMax;
    private final onTextFocusChanged setMin;

    public onActionViewExpanded(onTextFocusChanged ontextfocuschanged, onTextFocusChanged ontextfocuschanged2) {
        this.setMax = ontextfocuschanged;
        this.setMin = ontextfocuschanged2;
    }

    public final List<setTitleMarginTop<PointF>> setMin() {
        throw new UnsupportedOperationException("Cannot call getKeyframes on AnimatableSplitDimensionPathValue.");
    }

    public final boolean getMin() {
        return this.setMax.getMin() && this.setMin.getMin();
    }

    public final setInputType<PointF, PointF> getMax() {
        return new isIconified(new setOnQueryTextListener(this.setMax.setMin), new setOnQueryTextListener(this.setMin.setMin));
    }
}
