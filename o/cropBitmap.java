package o;

import id.dana.richview.exploredana.ExploreDanaView;
import o.Ignore;

public final class cropBitmap implements Ignore.setMin {
    private final ExploreDanaView getMax;

    public cropBitmap(ExploreDanaView exploreDanaView) {
        this.getMax = exploreDanaView;
    }

    public final void length(int i) {
        ExploreDanaView.getMax(this.getMax, i);
    }
}
