package o;

import id.dana.sendmoney.bank.savedcard.SavedBankCardView;
import o.Ignore;

public final class APSpecificCropMode implements Ignore.setMin {
    private final SavedBankCardView setMax;

    public APSpecificCropMode(SavedBankCardView savedBankCardView) {
        this.setMax = savedBankCardView;
    }

    public final void length(int i) {
        this.setMax.savedBankCardAdapter.setMin(i);
    }
}
